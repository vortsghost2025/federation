class PersistentEnsembleManager:
    """
    Manager for the entire persistent multi-AI ensemble system
    """
    
    def __init__(self, storage_path: str = "./ensemble_storage"):
        self.storage_path = Path(storage_path)
        self.storage_path.mkdir(exist_ok=True)
        
        # Multiple persistent agents
        self.agents = {}
        self.active_agents = []
        self.task_queue = []
        # Monitoring control flag
        self._monitoring_running = False
        
        # Initialize the primary persistent agent
        self.primary_agent = PersistentCodingAgent(
            agent_name="PrimaryEnsembleAgent",
            storage_path=str(self.storage_path / "primary_agent")
        )
        
        # Initialize specialized agents
        self.specialized_agents = {
            "mental_health": PersistentCodingAgent(
                agent_name="MentalHealthSpecialist",
                storage_path=str(self.storage_path / "mh_agent")
            ),
            "weather_tracking": PersistentCodingAgent(
                agent_name="WeatherTrackingSpecialist", 
                storage_path=str(self.storage_path / "wt_agent")
            ),
            "swarm_coordination": PersistentCodingAgent(
                agent_name="SwarmCoordinator",
                storage_path=str(self.storage_path / "swarm_agent")
            )
        }
        
        print(f"🏗️ Persistent Ensemble Manager initialized")
        print(f"💾 Storage path: {self.storage_path.absolute()}")
    
    def add_task_to_queue(self, task_description: str, priority: int = 1, target_agent: str = "auto"):
        """Add a task to the ensemble task queue"""
        task = {
            "id": f"task_{int(time.time())}",
            "description": task_description,
            "priority": priority,
            "target_agent": target_agent,
            "submitted_at": datetime.now(),
            "status": "queued"
        }
        
        self.task_queue.append(task)
        print(f"📥 Added task to queue: {task_description}")
        
        # Sort by priority (lower number = higher priority)
        self.task_queue.sort(key=lambda x: x["priority"])
        
        return task["id"]
    
    def process_task_queue(self):
        """Process all tasks in the queue"""
        if not self.task_queue:
            print("📋 Task queue is empty")
            return
        
        print(f"⚙️ Processing {len(self.task_queue)} tasks in queue...")
        
        for i, task in enumerate(self.task_queue[:]):  # Copy of list to avoid modification during iteration
            print(f"  Processing task {i+1}/{len(self.task_queue)}: {task['description']}")
            
            # Determine which agent to use
            agent = self._select_agent_for_task(task)
            
            if agent:
                # Execute the task
                project_context = {
                    "name": "Multi-AI Ensemble Project",
                    "structure": "distributed agents with swarm capabilities",
                    "requirements": ["mental health app", "weather tracking", "swarm coordination"]
                }
                
                result = agent.execute_task(task["description"], project_context)
                
                # Update task status
                task["status"] = "completed" if result["success"] else "failed"
                task["result"] = result
                
                print(f"  ✅ Task completed: {result['message']}")
            else:
                print(f"  ❌ No suitable agent found for task: {task['description']}")
                task["status"] = "failed"
        
        # Remove completed tasks
        self.task_queue = [task for task in self.task_queue if task["status"] != "completed"]
    
    def _select_agent_for_task(self, task: Dict[str, Any]) -> Optional[PersistentCodingAgent]:
        """Select the most appropriate agent for a given task"""
        target = task["target_agent"]
        
        if target == "auto":
            # Auto-select based on keywords in the task description
            desc_lower = task["description"].lower()
            
            if "mental health" in desc_lower or "therapy" in desc_lower or "wellness" in desc_lower:
                return self.specialized_agents["mental_health"]
            elif "weather" in desc_lower or "tracking" in desc_lower or "climate" in desc_lower or "noaa" in desc_lower:
                return self.specialized_agents["weather_tracking"]
            elif "swarm" in desc_lower or "ensemble" in desc_lower or "multi-ai" in desc_lower or "coordination" in desc_lower:
                return self.specialized_agents["swarm_coordination"]
            else:
                # Default to primary agent
                return self.primary_agent
        elif target in self.specialized_agents:
            return self.specialized_agents[target]
        elif target == "primary":
            return self.primary_agent
        else:
            # Unknown target, use primary
            return self.primary_agent
    
    def get_system_status(self) -> Dict[str, Any]:
        """Get the overall system status"""
        return {
            "timestamp": datetime.now().isoformat(),
            "primary_agent": self.primary_agent.get_memory_summary(),
            "specialized_agents": {
                name: agent.get_memory_summary() 
                for name, agent in self.specialized_agents.items()
            },
            "task_queue_length": len(self.task_queue),
            "active_agents": len(self.active_agents),
            "storage_path": str(self.storage_path.absolute())
        }
    
    def run_continuous_monitoring(self):
        """Run continuous monitoring and task processing"""
        print("🔍 Starting continuous monitoring...")
        try:
            self._monitoring_running = True
            while self._monitoring_running:
                # Process any queued tasks
                if self.task_queue:
                    print("\n🔄 Processing task queue...")
                    self.process_task_queue()

                # Print periodic status
                status = self.get_system_status()
                print(f"\n📊 System Status (Agents: {status['specialized_agents']['mental_health']['interaction_count']} interactions): "
                      f"Queue: {status['task_queue_length']}, Primary: {status['primary_agent']['interaction_count']} interactions")

                # Sleep for a bit before checking again
                # Sleep in small increments to be more responsive to stop requests
                for _ in range(30):
                    if not self._monitoring_running:
                        break
                    time.sleep(1)

        except KeyboardInterrupt:
            print("\n🛑 Continuous monitoring stopped by user")
        except Exception as e:
            print(f"\n❌ Error in continuous monitoring: {e}")
        finally:
            self._monitoring_running = False

    def stop_continuous_monitoring(self, timeout: int = 5) -> bool:
        """Signal the monitoring loop to stop and wait briefly for it to exit.

        Returns True if the flag was set (monitoring was running), False otherwise.
        """
        if not self._monitoring_running:
            return False

        print("🛑 Signaling continuous monitoring to stop...")
        self._monitoring_running = False
        # Caller-side thread handling (join) should be performed by the service wrapper
        return True

